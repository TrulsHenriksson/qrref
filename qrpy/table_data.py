def value_of_p(version: int, ec_level: ErrorCorrectionLevel) -> int:
    """Return the value of p as found in Table 9, page 38.

    Represents the number of misdecode protection bytes, see page 37 for a full
    explanation. Essentially, the last `p` error correction bytes are not able
    to correct more errors, only detect them. When the decoding would normally
    check `c - k` syndromes, we instead check `c - k - p` syndromes.
    """
    if version == 1:
        return {"L": 3, "M": 2, "Q": 1, "H": 1}[ec_level]
    if version == 2 and ec_level == "L":
        return 2
    if version == 3 and ec_level == "L":
        return 1
    return 0


def modules_per_side(version: int) -> int:
    return 17 + 4 * version

def function_pattern_modules(version: int) -> int:
    if version == 1:
        alignment_patterns_per_side = 0
        alignment_patterns = 0
    else:
        alignment_patterns_per_side = version // 7 + 2
        alignment_patterns = alignment_patterns_per_side ** 2 - 3

    alignment_patterns_on_timing_pattern = alignment_patterns_per_side - 2 if version != 1 else 0
    timing_modules_per_side = 1 + 4 * version - alignment_patterns_on_timing_pattern * 5

    finder_modules = 3 * 8**2
    alignment_modules = alignment_patterns * 5**2

    return finder_modules + timing_modules_per_side * 2 + alignment_modules

def format_and_version_modules(version: int) -> int:
    format_modules = 31
    version_modules = 0 if version <= 6 else 2 * 18
    return format_modules + version_modules

def data_modules(version: int) -> int:
    return (
        modules_per_side(version) ** 2
        - function_pattern_modules(version)
        - format_and_version_modules(version)
    )
